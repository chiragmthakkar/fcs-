class Person{
	private String name;
	private int age;

	// default constructor
	public Person(){
		this.name = "anon";
		this.age = -1;
	}

	// parameterised constructor
	public Person(String name, int age){
		this.name = name;
		this.age = age;
	}


	// accessors and mutators
	public void setAge(int age){
		this.age = age;
	}

	public void setName(String name){
		this.name = name;
	}
	public int getAge(){
		return this.age;
	}

	public String getName(){
		return this.name;
	}

	// 
	public String toString(){
		String t = "Person: " + getName() + " is age: " + getAge();
		return t;
	}
}