public class Problem
{
	public static void main(String[] args)
	{
		String expression="";
		for( int i = 0; i <= 40; i++ )
		{
			if(i < 10)
			{
				expression = "*";
			}
			else if(i < 20)
			{
				expression = "=";
			}
			else if(i < 30)
			{
				expression = "x";
			}
			else if(i < 40)
			{
				expression = "o";
			}

			switch( expression )
			{
				case "*":
					for( int j = 0; j < i; j++ )
					{
						System.out.print("*");
					}
					System.out.println("");
					break;

				case "=":
					for(int j = 0; j < i; j++ )
					{
						System.out.print("=");
					}
					System.out.println("");
					break;

				case "x":
					for( int j = 0; j < i; j++ )
					{
						System.out.print("x");
					}
					System.out.println("");
					break;

				case "o":
					for( int j = 0; j < i; j ++ )
					{
						System.out.print("o");
					}
					System.out.println("");
					break;
			}
		}
	}
}