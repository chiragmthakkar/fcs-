class Node{
	private Student info;
	private Node next;

	// default constructor
	public Node(){
		this.info = new Student();
		this.next = null;
	}

	// parameterised constructor
	public Node(Student tmpStudent){
		this.info = tmpStudent;
	}

	// accessors
	public Node getNext(){
		return this.next;
	}

	public Student getInfo(){
		return this.info;
	}

	// mutators
	public void setNext(Node next){
		this.next = next;
	}

	public void setInfo(Student info){
		this.info = info;
	}
}


